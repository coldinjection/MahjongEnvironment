function tiles2string(ts::TileList)
    str::String = ""
    for i = 1:length(ts)
        str *= EMOJIS[ts[i]]
    end
    return str
end

function string2tiles(str::String)
    ts::TileList = TileList([])
    for i in eachindex(str)
        push!(ts, SIJOME[str[i]])
    end
    return ts
end
